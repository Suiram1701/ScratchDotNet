namespace ScratchDotNet.Core.Enums;

/// <summary>
/// The default comparisons operators
/// </summary>
public enum ComparisonOperator
{
    /// <summary>
    /// The first value have to be greater than the second
    /// </summary>
    GreaterThan,

    /// <summary>
    /// The first value have to be less than the second
    /// </summary>
    LessThan,

    /// <summary>
    /// Both values have to be equal to each other
    /// </summary>
    Equals
}
